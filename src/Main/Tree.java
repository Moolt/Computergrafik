public class Tree{
    
    private float x, y, z;
    private float rotation;

    public Tree(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.rotation = (float) Math.random() * 360;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }

    public float getRotation() {
        return rotation;
    }

    
}
